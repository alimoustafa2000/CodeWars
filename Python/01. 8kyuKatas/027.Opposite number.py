# DESCRIPTION:
# Very simple, given an integer or a floating-point number, find its opposite.

# Examples:
# 1:   --> -1
# 14:  --> -14
# -34: --> 34

# ============
# Solution:
# ============

def opposite(number):

    return -number


print(opposite(1))
# -1

print(opposite(-25.6))
# 25.6

print(opposite(0))
# 0
